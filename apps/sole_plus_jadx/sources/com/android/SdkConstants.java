    public static String mkSdCardCmdName() {
        if (!System.getProperty("os.name").startsWith("Windows")) {
            return "mksdcard";
        }
        return "mksdcard.exe";
    }

    public static int currentPlatform() {
        String property = System.getProperty("os.name");
        if (property.startsWith("Mac OS")) {
            return 3;
        }
        if (property.startsWith("Windows")) {
            return 2;
        }
        return property.startsWith("Linux") ? 1 : 0;
    }

    public static String currentPlatformName() {
        String property = System.getProperty("os.name");
        if (property.startsWith("Mac OS")) {
            return "Mac OS X";
        }
        if (property.startsWith("Windows")) {
            return "Windows";
        }
        if (property.startsWith("Linux")) {
            return "Linux";
        }
        return "Other";
    }

    private static String ext(String windowsExtension, String nonWindowsExtension) {
        return CURRENT_PLATFORM == 2 ? windowsExtension : nonWindowsExtension;
    }

